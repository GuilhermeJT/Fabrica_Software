'''Faça um algoritmo que verifique se o número digitado é menor, maior ou igual a 10 e apresente a mensagem referente ao número.'''

num = int(input())

if num > 10:
    print("Esse número é maior que 10")

elif num == 10:
    print("Esse número é igual a 10")

else:
    print("Esse número é menor que 10")
